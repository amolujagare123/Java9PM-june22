package arraysDemos;

public class ArrayDemoChar {

    public static void main(String[] args) {

        char[] a = new char[5];

        a[0] = 'a';
        a[1] = 's';
        a[2] = 'd';
        a[3] = 'f';
        a[4] = 'f';



        for (int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }


    }
}
