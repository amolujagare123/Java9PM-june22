package arraysDemos;

public class ArrayDemoDouble {

    public static void main(String[] args) {

        double[] a = new double[5];

        a[0] = 1.1;
        a[1] = 2.1;
        a[2] = 1.3;
        a[3] = 4.1;
        a[4] = 1.7;



        for (int i=0;i<a.length;i++)
        {
            System.out.println(a[i]);
        }


    }
}
