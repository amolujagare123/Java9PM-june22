package InheritanceDemos.PolymorphismDemo;

public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double) a/b;
//if you want to convert one type of value into another
// use that type in the round bracket in front of the
// value to be converted - this is known as type casting


        System.out.println("c="+c);
    }
}
